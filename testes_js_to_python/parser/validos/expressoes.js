// Soma binária básica.
x + y;

// Mistura de multiplicação e subtração para exercitar precedência.
a * b - c;

// Comparações de igualdade e desigualdade.
x == y;
x != y;

// Comparações relacionais.
x > y;
x >= y;

// Operadores unários.
!x;
-y;

// Chamada com um e com vários argumentos.
f(x);
f(a, b, c);

// Acesso por propriedade e por índice.
obj.attr;
obj[0];

// Operadores lógicos binários.
x && y;
x || y;

// Parênteses explícitos numa expressão composta.
(x + y) * z;
