// Declaração simples com inicialização.
let x = 1;

// Atribuição composta deve ser aceita como expression statement.
x += 10;

// String literal em declaração.
let nome = "teste";

// Booleano deve ser aceito como primário.
let flag = true;

// null também é um literal válido na gramática.
let nada = null;

// const com número de ponto flutuante.
const PI = 3.14;

// var continua aceito para compatibilidade com o subconjunto JS.
var legacy = 0;

// let sem inicialização ainda é válido.
let empty;
