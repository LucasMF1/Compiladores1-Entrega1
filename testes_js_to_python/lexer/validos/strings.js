// Aspas simples e duplas basicas.
'Hello, world!'
"Hello, world!"

// Sequencias de escape comuns devem permanecer dentro do token STRING.
'This is a string with a newline character:\n'
"This is a string with a tab character:\t"
'This is a string with a backslash: \\'

// Unicode e emoji exercitam a copia do conteudo sem as aspas.
"This is a string with a Unicode character: \u263A"
'This is a string with an emoji: 😀'
